function _tlDetectLevel(vectors) {
    // Находим самый низкий вектор → самый проблемный
    const entries = Object.entries(vectors);
    const minVec = entries.sort((a,b) => a[1]-b[1])[0]?.[0];

    // Маппинг вектора на уровень Дилтса
    const map = {
        'СБ': 'identity',    // Низкий СБ → проблемы с Я-концепцией
        'ТФ': 'skills',      // Низкий ТФ → проблемы с навыками и реализацией
        'УБ': 'beliefs',     // Низкий УБ → проблемы с убеждениями
        'ЧВ': 'behavior'     // Низкий ЧВ → проблемы с поведением в отношениях
    };

    // Дополнительная логика: очень низкий УБ при высоком ЧВ → миссия
    if (vectors['УБ'] <= 2 && vectors['ЧВ'] >= 4) return 'mission';
    // Очень низкий ЧВ при высоком УБ → окружение
    if (vectors['ЧВ'] <= 2 && vectors['УБ'] >= 4) return 'environment';

    return map[minVec] || 'identity';
}


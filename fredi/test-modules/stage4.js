class Stage4Manager {
    constructor(core) {
        this.core = core;
        this.questions = Stage4Questions;
        this.intro = Stage4Intro;
        this.clarifyingQuestionsDB = ClarifyingQuestionsDB;
        this.discrepancyQuestions = DiscrepancyQuestions;
    }
    
    getQuestions() {
        return this.questions;
    }
    
    getIntro() {
        return this.intro;
    }
    
    getClarifyingQuestionsDB() {
        return this.clarifyingQuestionsDB;
    }
    
    getDiscrepancyQuestions() {
        return this.discrepancyQuestions;
    }
    
    handleAnswer(opt) {
        if (opt.dilts) {
            this.core.diltsCounts[opt.dilts] = (this.core.diltsCounts[opt.dilts] || 0) + 1;
        }
    }
    
    complete() {
        this.core.profileData = this.core.calculateFinalProfile();
        return this.core.profileData;
    }
    
    getGrowthTip() {
        const dominant = this.core.determineDominantDilts();
        const tips = {
            "ENVIRONMENT": "Посмотрите вокруг — может, дело в обстоятельствах?",
            "BEHAVIOR": "Попробуйте делать хоть что-то по-другому — маленькие шаги многое меняют.",
            "CAPABILITIES": "Развивайте новые навыки — они откроют новые возможности.",
            "VALUES": "Поймите, что для вас действительно важно — это изменит всё.",
            "IDENTITY": "Ответьте себе на вопрос «кто я?» — в этом ключ к изменениям."
        };
        return tips[dominant] || tips["BEHAVIOR"];
    }
}

